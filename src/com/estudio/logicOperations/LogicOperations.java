package com.estudio.logicOperations;

public class LogicOperations {
    int a;
    int b;

    // Método que verifica si el primer número es mayor que el segundo
    public boolean isGreaterThan(int a, int b) {
        return a > b;
    }

    // Método que verifica si el primer número es menor que el segundo
    public boolean isLessThan(int a, int b) {
        return a < b;
    }

    // Método que verifica si ambos números son iguales
    public boolean isEqualTo(int a, int b) {
        return a == b;
    }
}
